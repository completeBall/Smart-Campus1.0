function getDistractors(correct) {
  const wrong = [];
  const pool = distractorsPool.filter(w => w !== correct);
  while (wrong.length < 3 && pool.length > 0) {
    const idx = Math.floor(Math.random() * pool.length);
    wrong.push(pool.splice(idx, 1)[0]);
  }
  return JSON.stringify(wrong);
}

